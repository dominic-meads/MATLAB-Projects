function [s,p] = addmult(x,y) % Function [output variables] = name(input variables)
% addmult: computes the sum and product of two matricies
%
% function [s,p] = addmult(x,y)
% 
% Input Variables
% x = matrix input 1
% y = matrix input 2
%
% Output variables
% s = sum of the matricies
% p = product of the matricies
%
% Version 0.1 by Dominic Meads

s = x+y;
